package dataType;

public class NumericDataType {

	public static void main(String[] args) {
	
		// buying groceries from 100$ bill
		
		/*
		 * 	declaration
		 * int= data type
		 * apple= variable name
		 * = is operator
		 * 2= is value 
		 * ;= end of syntax
		 */
		
		int apple=2;	
		int milk=5;
		int totalSpent=apple + milk; 
		System.out.println(totalSpent);
		//2+5=7= total spend
		
		
		// buying merchandise
		
		double tv=1299.99;
		double soundBox=500.50;
		double totalMerchandise=tv+soundBox;
		System.out.println(totalMerchandise);
		
		
		// buying soccer
		
		int soccerBall=25;
		double jersy=65.99;
		double totalSoccer=soccerBall+jersy;
		//when you have mix data type---> chose bigger data type
		System.out.println(totalSoccer);
		
		

	}

}
